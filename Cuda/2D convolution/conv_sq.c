int clamp(int value, int min, int max) {
    if (value < min) return min;
    if (value > max) return max;
    return value;
}

void convolution(unsigned char image[HEIGHT][WIDTH][3], float kernel[KERNEL_SIZE][KERNEL_SIZE], unsigned char result[OUTPUT_HEIGHT][OUTPUT_WIDTH][3]) {

    for (int i = 0; i < OUTPUT_HEIGHT; i++) {
        for (int j = 0; j < OUTPUT_WIDTH; j++) {
            float sum[3] = {0.0, 0.0, 0.0};  // Sum for RGB channels

            // Apply the 3x3 kernel
            for (int ki = 0; ki < KERNEL_SIZE; ki++) {
                for (int kj = 0; kj < KERNEL_SIZE; kj++) {
                    int ni = clamp(i - ki, 0, HEIGHT - 1);  // Row boundary
                    int nj = clamp(j - kj, 0, WIDTH - 1);   // Column boundary

                    for (int c = 0; c < 3; c++) {
                        sum[c] += image[ni][nj][c] * kernel[ki][kj];
                    }
                }
            }

            // Store the blurred RGB value
            for (int c = 0; c < 3; c++) {
                result[i][j][c] = (unsigned char)sum[c];
            }

            printf("\x1b[48;2;%d;%d;%dm  ", result[i][j][0], result[i][j][1], result[i][j][2]);  // Position and color
        }
        printf("\x1b[0m\n");
    }
}

